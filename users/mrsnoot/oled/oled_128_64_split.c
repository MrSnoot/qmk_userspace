void render_layer_status(void) {

    bool hands_swapped = false;

#ifdef SWAP_HANDS_ENABLE
    extern bool swap_hands;
    hands_swapped = swap_hands;									   
#endif

    oled_write_P(PSTR("Layout: "), false);

    switch(biton32(default_layer_state))
    {
        case QWERTY:
            oled_write_P(PSTR("QWERTZ\n"), false);
            break;
        case OPY:
            oled_write_P(PSTR("OPY\n"), false);
            break;
    }

    // Host Keyboard Layer Status
    oled_write_P(PSTR("Layer:  "), hands_swapped);

    switch (get_highest_layer(layer_state)) {
        case QWERTY:
        case OPY:
            oled_write_P(PSTR("Basis\n"), hands_swapped);
            break;
        case SYMBOL:
            oled_write_P(PSTR("Symbol\n"), hands_swapped);
            break;
        case NUM_FUNC:
            oled_write_P(PSTR("Num / Fn\n"), hands_swapped);
            break;
        case NAV:
            oled_write_P(PSTR("Navigation\n"), hands_swapped);
            break;
		case ADJUST:
			oled_write_P(PSTR("RGB / Layout\n"), hands_swapped);
			break;
        default:
            oled_write_P(PSTR("Undefined\n"), hands_swapped);
    }
}

void render_led_status(void) {    
    // Host Keyboard LED Status
    led_t led_state = host_keyboard_led_state();
    oled_write_P(PSTR("\n"), false);
    oled_write_P(led_state.num_lock ? PSTR("NUM    ") : PSTR("       "), false);
    oled_write_P(led_state.caps_lock ? PSTR("CAPS   ") : PSTR("       "), false);
    oled_write_P(led_state.scroll_lock ? PSTR("SCR    ") : PSTR("       "), false);
}

void render_mods_status(void) {
    uint8_t mods = get_mods() | get_oneshot_mods();

    oled_write_P(mods & MOD_MASK_CTRL ? PSTR("CTRL ") : PSTR("     "), false);
    oled_write_P(mods & MOD_MASK_SHIFT ? PSTR("SHIFT ") : PSTR("      "), false);
    oled_write_P(mods & MOD_MASK_ALT ? PSTR("ALT ") : PSTR("    "), false);
    oled_write_ln_P(mods & MOD_MASK_GUI ? PSTR("GUI ") : PSTR("    "), false);
}

void render_trackball_status(void) {
    oled_write_P(PSTR("DPI Mv/Scr: "), false);

#ifdef RAW_ENABLE
    oled_write(tb_dpi_move, false);
#else
    oled_write_P(PSTR("--"), false);
#endif

    oled_write_P(PSTR("/"), false);

#ifdef RAW_ENABLE
    oled_write(tb_dpi_scroll, false);
#else
    oled_write_P(PSTR("--"), false);
#endif
}

static void render_master_oled(void) {
    render_layer_status();
    render_led_status();
    render_mods_status();
    render_trackball_status();

    if (timer_elapsed(invert_oled_timer) > 6000) {
        oled_invert(invert_oled);
        invert_oled = !invert_oled;
        invert_oled_timer = timer_read();
    }
}

void render_slave_oled(void) {
    render_space_logo();

    if (timer_elapsed(invert_oled_timer) > 6000) {
        oled_invert(invert_oled);
        invert_oled = !invert_oled;
        invert_oled_timer = timer_read();
    }
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_master_oled(); // Renders the current keyboard state (layer, lock, caps, scroll, etc)
    } else {
        render_slave_oled();
    }

    return false;
}
