bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_master_logo();
        render_space();
        render_layer_state();
        render_space();
        render_mod_status_gui_alt(get_mods()|get_oneshot_mods());
        render_mod_status_ctrl_shift(get_mods()|get_oneshot_mods());
        render_space();
        render_kb_LED_state();
        #ifdef CAPS_WORD_ENABLE
        render_caps_word_state();
        #endif
        #ifdef WPM_ENABLE
        #ifdef OLED_SLAVE_IMAGE_ONLY
        render_wpm();
        #endif
        #endif
    } else {
        render_slave_logo();
        #ifdef WPM_ENABLE
        #ifndef OLED_SLAVE_IMAGE_ONLY
        render_wpm();
        #endif
        #endif
    }
 
    return false;
}
